// prime number 
#include<iostream>
using namespace std;
int main(){
    int n,flag=0;
    cout<<"Enter number : ";
    cin>>n;
    for(int i=2;i<=n/2;i++){
        if(n%i==0)
        flag = 1;
    } 
     if(flag==1)
        cout<<"Not a prime number";
        else
        cout<<"Prime number"; 
    return 0;
}  